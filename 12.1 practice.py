'''Python if 嵌套编程题：学生成绩评级系统
请编写一个 Python 程序，根据学生的总成绩和是否为班干部两个条件，使用 if 嵌套结构来确定最终评级，具体规则如下：
首先判断总成绩（满分 100 分）：
若成绩 ≥ 90 分，进入 “优秀候选”；
若 90 分 > 成绩 ≥ 75 分，进入 “良好候选”；
若 75 分 > 成绩 ≥ 60 分，进入 “及格候选”；
若成绩 <60 分，直接评级为 “不及格”。
对 “优秀候选”“良好候选”“及格候选”，进一步结合 “是否为班干部”（输入 “是” 或 “否”）判断最终评级：
优秀候选：若为班干部，最终评级 “优秀（班干部）”；若不是，评级 “优秀”。
良好候选：若为班干部，最终评级 “良好（班干部）”；若不是，评级 “良好”。
及格候选：若为班干部，最终评级 “及格（班干部）”；若不是，评级 “及格”。
要求：
从键盘获取学生的 “总成绩”（整数）和 “是否为班干部”（字符串）；
必须使用 if 嵌套结构实现逻辑判断；
输出最终评级结果（例如：“该学生最终评级为：优秀（班干部）”）。'''

Score = float(input('你的分数是多少分呢:'))
Bangan = input('你是班干部吗(请回答是或不是):')
if 90 <= Score <=100:
    if Bangan == '是':
        print('优秀班干部')
    elif Bangan == '不是':
        print('优秀')
    else:
        print('输入有误，请回答是或不是')
elif 75<= Score < 90:
    if Bangan == '是':
        print('良好班干部')
    elif Bangan == '不是':
        print('良好')
    else:
        print('输入有误，请回答是或不是')
elif 60<= Score < 75:
    if Bangan == '是':
        print('及格班干部')
    elif Bangan == '不是':
        print('及格')
    else:
        print('输入有误，请回答是或不是')
elif 0<= Score <60:
    print('孩子你不及格啊')
else:
    print('分数存在错误，分数范围为0-100，请重新输入！')
